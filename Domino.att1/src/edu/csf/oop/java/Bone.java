package edu.csf.oop.java;
public class Bone {
    private int point1;
    private int point2;

    public Bone(int point1, int point2) {
        this.point1 = point1;
        this.point2 = point2;
    }


    public int getPoint1() {
        return point1;
    }
    public int getPoint2() {
        return point2;
    }
}